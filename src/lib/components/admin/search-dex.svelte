<script lang="ts">
    import { onMount } from "svelte";

        export let selected: FullDexEntry;
        let fullDex: FullDexEntry[] = [];
        let isOpen = false;
        let searchValue = '';
        
        // Function to toggle the dropdown state
        const toggleDropdown = () => {
          isOpen = !isOpen;
        };

        $:filtered = searchValue && searchValue?.length > 2 ? fullDex.filter((entry) => {
            return entry?.name?.english.includes(searchValue);
        }) : fullDex?.slice(0, 10);
        
        onMount(() => {
            fetch("/base-pokedex.json")
                .then((response) => response.json())
                .then((data) => {
                    fullDex = data;
                    console.log(fullDex);
                });
        });
</script>

<div class="relative group">
    <button on:click={toggleDropdown} id="dropdown-button" class="inline-flex justify-center w-full px-4 py-2 text-sm font-medium text-gray-700 bg-white border border-gray-300 rounded-md shadow-sm focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-offset-gray-100 focus:ring-blue-500">
      <span class="mr-2">Open Dropdown</span>
      <svg xmlns="http://www.w3.org/2000/svg" class="w-5 h-5 ml-2 -mr-1" viewBox="0 0 20 20" fill="currentColor" aria-hidden="true">
        <path fill-rule="evenodd" d="M6.293 9.293a1 1 0 011.414 0L10 11.586l2.293-2.293a1 1 0 111.414 1.414l-3 3a1 1 0 01-1.414 0l-3-3a1 1 0 010-1.414z" clip-rule="evenodd" />
      </svg>
    </button>
    <div id="dropdown-menu" class="{isOpen ? 'absolute right-0 mt-2 rounded-md shadow-lg bg-white ring-1 ring-black ring-opacity-5 p-1 space-y-1' : 'hidden absolute right-0 mt-2 rounded-md shadow-lg bg-white ring-1 ring-black ring-opacity-5 p-1 space-y-1'}">
      <!-- Search input -->
      <input bind:value={searchValue} id="search-input" class="block w-full px-4 py-2 text-gray-800 border rounded-md  border-gray-300 focus:outline-none" type="text" placeholder="Search items" autocomplete="off">
      <!-- Dropdown content goes here -->
        {#each filtered as entry}
            <span on:click={() => selected = entry} class="block px-4 py-2 text-gray-700 hover:bg-gray-100 active:bg-blue-100 cursor-pointer rounded-md">{entry.name?.english}</span>
        {/each}

    </div>
  </div>